def greet(first_name, last_name, age, city, country):

    print('Hello {} {}, How are you ?'.format(first_name, last_name))
    print('Your age is {}. ?'.format(age))
    print('Your City is {}.'.format(city))
    print('Your Country is {}'.format(country))



greet( 'Shahid', 'Ghafoor', 21, 'Multan', 'Pakistan' )